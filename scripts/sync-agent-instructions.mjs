import { readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { resolve } from "node:path";

const root = process.cwd();
const sourcePath = resolve(root, ".ruler", "AGENTS.md");
const targets = ["AGENTS.md", "CLAUDE.md"].map((path) => resolve(root, path));

const mode = process.argv.includes("--check") ? "check" : "write";
const source = await readFile(sourcePath, "utf8");
const normalizedSource = source.replace(/\r\n/g, "\n").replace(/\n*$/u, "\n");
const expected = `<!-- Generated by Ruler -->\n\n<!-- Source: .ruler/AGENTS.md -->\n\n${normalizedSource}`;

const stale = [];

for (const target of targets) {
  if (mode === "write") {
    await writeFile(target, expected, "utf8");
    continue;
  }

  if (!existsSync(target)) {
    stale.push(target);
    continue;
  }

  const actual = (await readFile(target, "utf8")).replace(/\r\n/g, "\n");
  if (actual !== expected) {
    stale.push(target);
  }
}

if (stale.length > 0) {
  console.error(
    [
      "Agent startup files are out of sync with .ruler/AGENTS.md.",
      "Run `npm run agents:sync` and commit the generated files.",
      "",
      ...stale.map((path) => `- ${path}`),
    ].join("\n"),
  );
  process.exit(1);
}
