import { readFile, readdir, rm, writeFile } from "node:fs/promises";
import { join } from "node:path";
import { spawn } from "node:child_process";

const generatedStartupFiles = ["AGENTS.md", "CLAUDE.md"];
const startupFilesToNormalize = generatedStartupFiles;
const generatedAgentDirs = [".claude/agents"];
const generatedCodexAgentDir = ".codex/agents";

for (const path of generatedStartupFiles) {
  await rm(path, { force: true });
}

const isWindows = process.platform === "win32";
const command = isWindows ? "cmd.exe" : "ruler";
const args = isWindows
  ? ["/d", "/s", "/c", "ruler.cmd apply --agents claude,codex --skills --subagents"]
  : ["apply", "--agents", "claude,codex", "--skills", "--subagents"];

const child = spawn(command, args, { stdio: "inherit" });

const exitCode = await new Promise((resolve) => {
  child.on("exit", (code) => resolve(code ?? 1));
  child.on("error", () => resolve(1));
});

if (exitCode !== 0) {
  process.exitCode = exitCode;
} else {
  for (const path of startupFilesToNormalize) {
    await normalizeGeneratedStartupFile(path);
  }
  await normalizeGeneratedMarkdownAgents();
  await normalizeGeneratedCodexAgents();
}

async function normalizeGeneratedStartupFile(path) {
  const text = await readFile(path, "utf8");
  const bodyStart = text.indexOf("# uTradeHub Automation Agent Instructions");
  if (bodyStart < 0) return;
  const body = compactMarkdown(text.slice(bodyStart));
  const normalized = [
    "<!-- Generated by Ruler -->",
    "<!-- Source: .ruler/AGENTS.md -->",
    body,
  ].join("\n");
  await writeFile(path, normalized, "utf8");
}

async function normalizeGeneratedMarkdownAgents() {
  for (const dir of generatedAgentDirs) {
    const entries = await readdir(dir, { withFileTypes: true });
    for (const entry of entries) {
      if (!entry.isFile() || !entry.name.endsWith(".md")) continue;
      const path = join(dir, entry.name);
      const text = await readFile(path, "utf8");
      const bodyStart = text.indexOf("---\n", 4);
      if (bodyStart < 0) continue;
      const frontmatter = text.slice(0, bodyStart + 4).replace(/\s*$/u, "\n");
      const body = compactMarkdown(text.slice(bodyStart + 4));
      await writeFile(path, `${frontmatter}${body}`, "utf8");
    }
  }
}

async function normalizeGeneratedCodexAgents() {
  const entries = await readdir(generatedCodexAgentDir, { withFileTypes: true });
  for (const entry of entries) {
    if (!entry.isFile() || !entry.name.endsWith(".toml")) continue;
    const path = join(generatedCodexAgentDir, entry.name);
    const text = await readFile(path, "utf8");
    const normalized = text.replace(
      /developer_instructions = """\n([\s\S]*?)\n"""/u,
      (_match, body) => `developer_instructions = """\n${compactMarkdown(body)}"""`,
    );
    await writeFile(path, normalized.replace(/\s*$/u, "\n"), "utf8");
  }
}

function compactMarkdown(text) {
  const lines = text.replace(/\r\n/g, "\n").split("\n");
  const compactLines = [];

  for (const line of lines) {
    if (line.trim() === "") continue;
    compactLines.push(line.replace(/\s+$/u, ""));
  }

  return `${compactLines.join("\n")}\n`;
}
